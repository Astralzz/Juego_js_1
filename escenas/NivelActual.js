//Pause
let Pause = false;
export function isPause() {
  Pause = !Pause;
}

//Crear matriz de un ventor
function arrayMatriz(vector, Filas, Columnas) {
  //Comprobamos
  if (vector.length !== Filas * Columnas) {
    return null;
  }

  //Matriz
  const Matriz = [];
  let aux = 0;

  //Bucle para dibujar el nivel
  for (let i = 0; i < Filas; i++) {
    Matriz.push([]);
    for (let j = 0; j < Columnas; j++) {
      Matriz[i].push(vector[aux]);
      aux++;
    }
  }

  return Matriz;
}

//Actualizar
function actualizarTodo() {
  //Obtenemos matriz
  MATRIZ = arrayMatriz(escogerNivel(), FILAS, COLUMNAS);
  //Escogemos bloque
  BloqueEscogido.src = Bloque;
}

//Bucle principal
function loop() {
  requestAnimationFrame(loop);

  //Actualizamos
  actualizarTodo();

  //Verificamos vidas
  if (verificarVidas()) {
    //Perdimos
    return;
  }

  //Verificamos pausa
  console.log(Pause);
  if (Pause) {
    //Perdimos
    return;
  }

  //Tamaños
  mundo.width = 800;
  mundo.height = 608;

  //Ponemos la imagen
  ctx.drawImage(Fondo1, 0, 0, 800, 608);

  //Bucle para dibujar el nivel
  for (let y = 0; y < FILAS; y++) {
    for (let x = 0; x < COLUMNAS; x++) {
      //Si no es 0
      if (MATRIZ[y][x] === 1) {
        //Ponemos la imagen
        ctx.drawImage(BloqueEscogido, x * 32, y * 32, 32, 32);
      }
    }
  }

  //Cuadro
  Cuadro.pintarCuadro();
  Vida.DibujarVidas();
  Vida.colisiónConEnemigos();
  Puntos.dibujarPuntuaje();
  Puntos.colisiónConCopa();

  //Copa
  Copa1.draw();

  //Recorremos los enemigos
  EnemigosArray.forEach((c) => {
    c.enm.draw();
    //Vemos tipo
    if (c.tipo !== 1) {
      c.enm.moverDerechaIzquierda();
    } else {
      c.enm.moverArribaAbajo();
    }
  });

  //Ninja dibujar
  ninja1.draw();
}

